class Persona{
    id:Number;
    nombre:String;
    apellido:String;
    email:String;
    edad:Number;
    sexo:String

    constructor(id:Number, nombre:String, apellido:String, email:String, edad:Number, sexo:String){
        this.id=id;
        this.nombre=nombre;
        this.apellido=apellido;
        this.email=email;
        this.edad=edad;
        this.sexo=sexo;
    }
}